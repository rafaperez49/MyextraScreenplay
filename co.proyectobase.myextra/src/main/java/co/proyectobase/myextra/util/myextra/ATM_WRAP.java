package co.proyectobase.myextra.util.myextra  ;

/**
 * <p>
 * Screen wrapping characteristics.
 * </p>
 */
public enum ATM_WRAP {
  /**
   * <p>
   * Limits operations to beginning and end of the screen.
   * </p>
   * <p>
   * The value of this constant is 0
   * </p>
   */
  ATM_WRAP_OFF, // 0
  /**
   * <p>
   * Operations can wrap at the end of screen if necessary.
   * </p>
   * <p>
   * The value of this constant is 1
   * </p>
   */
  ATM_WRAP_ON, // 1
}
